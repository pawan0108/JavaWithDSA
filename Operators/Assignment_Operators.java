public class Assignment_Operators {
    public static void main(String[] args) {
        float A = 10;
        System.out.println(A);
         A += 11;
         System.out.println(A);
         A -= 7;
         System.out.println(A);
         A *= 6;
         System.out.println(A);
         A /= 4;
         System.out.println(A);
        A %= 5;
         System.out.println(A);
    }
}
