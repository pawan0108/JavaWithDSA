public class Logical_Operators {
    public static void main(String[] args) {

        // Logical AND

        System.out.println(6 > 5 && 4 < 8);
        System.out.println(6 > 9 && 4 < 8);

        // Logical OR

        System.out.println(6 > 9 || 4 < 8);

        // Logical NOT

        System.out.println(!(6 > 5));
        System.out.println(!(1 > 5));
    }
}
