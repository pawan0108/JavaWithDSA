import java.util.*;

public class Products {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter a Number for a : ");
        int a = sc.nextInt();
        System.out.print("Enter a Number for b : ");
        int b = sc.nextInt();
        int c = a * b;
        System.out.print("Product of a * b = " + c);

    }
}
